i=0
def fun(n):
    global i

    if(n%2==0):
        n=n/2
        fun(n)

        return i+1

    elif (n%2!=0 and n!=1):
        n=(3*n+1)/2
        fun(n)
        return i+1
    elif (n==1):
        return i+1

s=fun(10)
print(s)


