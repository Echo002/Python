deal = []
count = 1
num = int(input())
for i in range(num):
    list = input().split()
    for i in range(len(list)):
        list[i] = int(list[i])
    deal.append(list)
for i in deal:
    if i[2] > i[0] + i[1]:
        print("Case #"+ str(count) + ": true")
    else:
        print("Case #"+ str(count) + ": false")
    count += 1

