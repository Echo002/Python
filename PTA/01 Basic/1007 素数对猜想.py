n = int(input())
num=[]
count = 0
for i in range(2,n):
    j = 2
    for j in range(2, i):
        if (i % j == 0):
            break
    else:
        num.append(i)
for k in range(1,len(num)):
    if(num[k] - num[k-1] == 2):
        count += 1
print(count)
